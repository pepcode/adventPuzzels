public class Passport {
    private String birthYr;
    private String issueYr;
    private String expiryYr;
    private String height;
    private String hairClr;
    private String eyeClr;
    private String passportId;
    private String countryId;

    public String getBirthYr() {
        return birthYr;
    }

    public void setBirthYr(String birthYr) {
        this.birthYr = birthYr;
    }

    public String getIssueYr() {
        return issueYr;
    }

    public void setIssueYr(String issueYr) {
        this.issueYr = issueYr;
    }

    public String getExpiryYr() {
        return expiryYr;
    }

    public void setExpiryYr(String expiryYr) {
        this.expiryYr = expiryYr;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getHairClr() {
        return hairClr;
    }

    public void setHairClr(String hairClr) {
        this.hairClr = hairClr;
    }

    public String getEyeClr() {
        return eyeClr;
    }

    public void setEyeClr(String eyeClr) {
        this.eyeClr = eyeClr;
    }

    public String getPassportId() {
        return passportId;
    }

    public void setPassportId(String passportId) {
        this.passportId = passportId;
    }

    public String getCountryId() {
        return countryId;
    }

    public void setCountryId(String countryId) {
        this.countryId = countryId;
    }

    
}